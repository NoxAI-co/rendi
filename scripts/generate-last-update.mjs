import { statSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");

const SOURCE = resolve(ROOT, "app/_DATA/Banks.ts");
const OUTPUT = resolve(ROOT, "lib/last-update.ts");

const mtime = statSync(SOURCE).mtime;

const day = String(mtime.getDate()).padStart(2, "0");
const month = String(mtime.getMonth() + 1).padStart(2, "0");
const year = mtime.getFullYear();
const formatted = `${day}/${month}/${year}`;

const contents = `// AUTO-GENERATED by scripts/generate-last-update.mjs — do not edit by hand.
export const LAST_UPDATE = "${formatted}";
`;

mkdirSync(dirname(OUTPUT), { recursive: true });
writeFileSync(OUTPUT, contents, "utf8");

console.log(`[last-update] ${formatted} (from ${SOURCE})`);
